<?php

namespace App\Traits;

use App\Models\User;

trait HasUserTrait
{
    /** @var User */
    private $user;

    /**
     * @return User
     */
    public function getUser(): User
    {
        return $this->user;
    }

    /**
     * @param User $user
     */
    public function setUser(User $user): void
    {
        $this->user = $user;
    }
}
